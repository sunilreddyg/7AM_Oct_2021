package mq.java.conditional_statements;

public class Else_if_Statement {

	public static void main(String[] args) 
	{
		//Multile decision statements take place here
		String browser="chrome";
		
		if (browser.equals("chrome")) 
		{
			System.out.println("Chrome testing");
		} 
		else if (browser.equals("firefox"))
		{
			System.out.println("Firefox testing");
		}
		else if (browser.equals("ie")) 
		{
			System.out.println("ie testing");
		}
		else if (browser.equals("safari")) 
		{
			System.out.println("safari testing");
		}
		else 
		{
			System.out.println("No browser is matching");
		}
	 
	 
	 
	 
	 

	}

}
